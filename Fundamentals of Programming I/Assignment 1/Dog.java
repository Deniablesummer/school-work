/*
 * Dog.java
 * Displays a dog
 * Outputs ASCII characters in the shape of a dog
 */
public class Dog {

	public static void main(String[] args) {
		System.out.println("   ''',");
		
		System.out.println("o_)0 \\)____\"");
		System.out.println(" \\_        )");
		System.out.println("   '',,,,,,");
		System.out.println("     ||  ||");
		System.out.println("    \"--'\"--'");
		//Output is a dog

	}

}
